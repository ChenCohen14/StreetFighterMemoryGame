package com.example.win10.streetfighter;



public class Player {

    private String playerName;
    private Double score;

    public Player(String name, Double score){
        this.playerName = name;
        this.score = score;

    }

    public String getplayerName() {
        return playerName;
    }

    public Double getScore() {
        return score;
    }





}
